-- Public read policies
drop policy if exists "public read tours" on public.tours;
create policy "public read tours"
on public.tours for select
using (is_published = true);

drop policy if exists "public read tour items" on public.tour_program_items;
create policy "public read tour items"
on public.tour_program_items for select
using (exists (
  select 1 from public.tours t where t.id = tour_program_items.tour_id and t.is_published = true
));

drop policy if exists "public read home content" on public.home_content;
create policy "public read home content"
on public.home_content for select
using (true);

-- Authenticated admin policies via app_metadata role
drop policy if exists "admin full leads" on public.leads;
create policy "admin full leads"
on public.leads for all
using ((auth.jwt() -> 'app_metadata' ->> 'role') = 'admin')
with check ((auth.jwt() -> 'app_metadata' ->> 'role') = 'admin');

drop policy if exists "admin full tours" on public.tours;
