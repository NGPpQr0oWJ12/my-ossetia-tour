-- updated_at trigger
create or replace function public.set_updated_at()
returns trigger
language plpgsql
as $$
begin
  new.updated_at = now();
  return new;
end;
$$;

drop trigger if exists set_updated_at_leads on public.leads;
create trigger set_updated_at_leads
before update on public.leads
for each row execute function public.set_updated_at();

drop trigger if exists set_updated_at_tours on public.tours;
create trigger set_updated_at_tours
before update on public.tours
for each row execute function public.set_updated_at();

drop trigger if exists set_updated_at_home on public.home_content;
create trigger set_updated_at_home
before update on public.home_content
for each row execute function public.set_updated_at();

