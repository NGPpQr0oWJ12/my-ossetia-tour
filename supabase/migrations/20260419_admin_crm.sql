-- Enable extensions
create extension if not exists pgcrypto;

-- Leads
create table if not exists public.leads (
  id bigint generated by default as identity primary key,
  source_page text not null,
  tour_id bigint null references public.tours(id) on delete set null,
  name text not null,
  phone text not null,
  message text null,
  status text not null default 'new' check (status in ('new', 'in_progress', 'booked', 'closed_lost')),
  manager_comment text null,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

-- Tours
create table if not exists public.tours (
  id bigint generated by default as identity primary key,
  slug text unique not null,
  title text not null,
  short_description text not null,
  full_description text not null,
  price_from text not null,
  duration text not null,
  group_size text not null,
  location text not null,
  cover_image_url text not null,
  gallery text[] not null default '{}',
  is_published boolean not null default true,
  sort_order int not null default 0,
  seo_title text not null,
  seo_description text not null,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

-- Program items
create table if not exists public.tour_program_items (
  id bigint generated by default as identity primary key,
  tour_id bigint not null references public.tours(id) on delete cascade,
  position int not null default 0,
  title text not null,
  description text not null,
  image_url text not null,
  created_at timestamptz not null default now()
);

