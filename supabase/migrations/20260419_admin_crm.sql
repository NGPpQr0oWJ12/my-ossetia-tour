-- Storage
insert into storage.buckets (id, name, public)
values ('crm-media', 'crm-media', true)
on conflict (id) do nothing;

drop policy if exists "public read crm media" on storage.objects;
create policy "public read crm media"
on storage.objects for select
using (bucket_id = 'crm-media');

drop policy if exists "admin upload crm media" on storage.objects;
create policy "admin upload crm media"
on storage.objects for insert
with check (bucket_id = 'crm-media' and (auth.jwt() -> 'app_metadata' ->> 'role') = 'admin');

drop policy if exists "admin update crm media" on storage.objects;
create policy "admin update crm media"
on storage.objects for update
using (bucket_id = 'crm-media' and (auth.jwt() -> 'app_metadata' ->> 'role') = 'admin');

drop policy if exists "admin delete crm media" on storage.objects;
create policy "admin delete crm media"
on storage.objects for delete
using (bucket_id = 'crm-media' and (auth.jwt() -> 'app_metadata' ->> 'role') = 'admin');
